input_ooo = input("Введите три целых числа через пробел: ")
numbers = input_ooo.split()
li = int(numbers[0])
kov = int(numbers[1])
CA = int(numbers[2])
sa1 = li * kov
sa2 = kov * CA
sa3 = CA * li
step = li ** 4
ost = kov % CA
cel = CA // li
print(f"li * kov = {sa1}")
print(f"kov * CA = {sa2}")
print(f"CA * li = {sa3}")
print(f"li в 4 степени = {step}")
print(f"остаток от  kov на CA = {ost}")
print(f"целочисленное деление CA на li = {cel}")
print(f"step + ost + cel = {step + ost + cel}")